    public ArrayList<listTopic> listTopic() {
        ArrayList<listTopic> ltTopic = new ArrayList<>();
        try {
            String url = "Select * from ChuDe";
            st = con.createStatement();
            rs = st.executeQuery(url);
            listTopic data;
            while (rs.next()) {
                data = new listTopic(rs.getString("TenChuDe"));
                ltTopic.add(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return ltTopic;
    }

