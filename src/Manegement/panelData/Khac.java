    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtTenChuDe.getText().equals("") && txtTenCapDo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không được bỏ trống!");
        } else {
            if (!txtTenChuDe.getText().equals("")) {
                try {
                    String url = "insert into ChuDe values (?)";
                    pst = con.prepareStatement(url);
                    pst.setString(1, txtTenChuDe.getText());
                    pst.executeUpdate();
                    DefaultTableModel model = (DefaultTableModel) tblTopic.getModel();
                    model.setRowCount(0);
                    show_Topic();
                    JOptionPane.showMessageDialog(null, "Lưu dữ liệu thành công.");
                    btnAdd.setEnabled(true);
                    dataEnabledButton();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
            if (!txtTenCapDo.getText().equals("")) {
                try {
                    String url = "insert into CapDo values (?)";
                    pst = con.prepareStatement(url);
                    pst.setString(1, txtTenCapDo.getText());
                    pst.executeUpdate();
                    DefaultTableModel model = (DefaultTableModel) tblLevel.getModel();
                    model.setRowCount(0);
                    show_Level();
                    JOptionPane.showMessageDialog(null, "Lưu dữ liệu thành công.");
                    btnAdd.setEnabled(true);
                    dataEnabledButton();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtTenChuDe.getText().equals("") && txtTenCapDo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không được bỏ trống!");
        } else {
            if (!txtTenChuDe.getText().equals("")) {
                try {
                    pst = con.prepareStatement("Delete ChuDe where TenChuDe=?");
                    pst.setString(1, tblTopic.getValueAt(tblTopic.getSelectedRow(), 0).toString());
                    if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        pst.executeUpdate();
                        DefaultTableModel model = (DefaultTableModel) tblTopic.getModel();
                        model.setRowCount(0);
                        show_Topic();
                        JOptionPane.showMessageDialog(this, "Xóa thành công");
                    }
                    btnAdd.setEnabled(true);
                    dataEnabledButton();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                }
            }
            else if (!txtTenCapDo.getText().equals("")) {
                try {
                    pst = con.prepareStatement("Delete ChuDe where TenCapDo=?");
                    pst.setString(1, tblLevel.getValueAt(tblLevel.getSelectedRow(), 0).toString());
                    if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        pst.executeUpdate();
                        DefaultTableModel model = (DefaultTableModel) tblLevel.getModel();
                        model.setRowCount(0);
                        show_Level();
                        JOptionPane.showMessageDialog(this, "Xóa thành công");
                    }
                    btnAdd.setEnabled(true);
                    dataEnabledButton();
                    reset();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

