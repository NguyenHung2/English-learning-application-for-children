    public void loadDataCbbMaBKT() {
        try {
            pst = con.prepareStatement("Select * from BaiKiemTra");
            rs = pst.executeQuery();

            //Tạo 1 DefaultComboboxModel
            DefaultComboBoxModel SeMSSV = (DefaultComboBoxModel) cbbMaBKT.getModel();
            SeMSSV.removeAllElements(); //Xóa hết dữ liệu trong combobox
            while (rs.next()) {
                cbbMaBKT.addItem(rs.getString("MaBKT"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi lấy maND!");
        }
    }

